# variáveis em python são bastante flexíveis!

variavel = 1.2
print(type(variavel), variavel, '\n')

variavel = 1
print(type(variavel), variavel, '\n')

variavel = 'flor'
print(type(variavel), variavel, '\n')

variavel = ['um', 'dois', 'três']
print(type(variavel), variavel, '\n')

variavel = ('um', 2, 'três')
print(type(variavel), variavel, '\n')

variavel = 5j
print(type(variavel), variavel, '\n')

variavel = {'aleatório': ['humano', 'cachorro'], 'teste': ['onamuh', 'orrohcac']}
print(type(variavel), variavel, '\n')