# a python variable is really flexible!

variable = 1.2
print(type(variable), variable, '\n')

variable = 1
print(type(variable), variable, '\n')

variable = 'flower'
print(type(variable), variable, '\n')

variable = ['one', 'two', 'three']
print(type(variable), variable, '\n')

variable = ('one', 2, 'three')
print(type(variable), variable, '\n')

variable = 5j
print(type(variable), variable, '\n')

variable = {'random': ['human', 'dog'], 'test': ['namuh', 'god']}
print(type(variable), variable, '\n')